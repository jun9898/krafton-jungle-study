n = int(input())

print(("*"*n)+(" "*((n*2)-3))+("*"*n))

for i in range(1, n):
    if i == n-1:
        print((" "*i)+("*")+(" "*(n-2))+("*") + (" "*((n*2)-3-(i*2))),end="")
        print((" "*(n-2))+("*"))
    else:
        print((" "*i)+("*")+(" "*(n-2))+("*") + (" "*((n*2)-3-(i*2))),end="")
        print(("*")+(" "*(n-2))+("*"))

for i in range(n-2,0,-1):
    print((" "*i)+("*")+(" "*(n-2))+("*") + (" "*((n*2)-3-(i*2))),end="")
    print(("*")+(" "*(n-2))+("*"))

print(("*"*n)+(" "*((n*2)-3))+("*"*n))